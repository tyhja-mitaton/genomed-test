<?php

namespace app\controllers;

use app\models\Deeplink;
use app\models\Log;
use Yii;
use yii\filters\AccessControl;
use yii\helpers\Html;
use yii\helpers\Url;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\web\Response;
use yii\filters\VerbFilter;
use app\models\LoginForm;
use app\models\ContactForm;

class SiteController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::class,
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::class,
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        $model = new Deeplink();
        if(\Yii::$app->request->isAjax) {
            if($model->load(Yii::$app->request->post()) && $model->validate()) {
                $model->link_id = \Yii::$app->security->generateRandomString(10);
                $model->save();
            }
            return $this->renderAjax('generator', ['model' => $model]);
        }
        return $this->render('index', ['model' => $model]);
    }

    public function actionShort($id)
    {
        $deeplink = Deeplink::findOne(['link_id' => $id]);
        if(!empty($deeplink)) {
            $deeplink->updateCounters(['counter' => 1]);
            Log::create($deeplink->id, Yii::$app->request->userIP);
            return $this->redirect($deeplink->url);
        } else {
            throw new NotFoundHttpException('Target URL not found');
        }
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }
}
